public class Player {
    String name;
    int health;
    int ac;
    String clas;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getHealth() {
        return this.health;
    }

    public void setAC(int ac) {
        this.ac = ac;
    }

    public int getAC() {
        return this.ac;
    }

    public void setClas(String clas) {
        this.clas = clas;
    }

    public String getClas() {
        return this.clas;
    }
}

